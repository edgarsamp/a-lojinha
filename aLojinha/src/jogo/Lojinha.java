    private void lblBotaoComprarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBotaoComprarMouseReleased

        lblBotaoComprar.setBounds(560, 160, 450, 60);

        if(carrinho == 0){ // se nao comprou nada
            setVisible(false);
            if (Inicial.jogador.getLevelAtual() <= 10) {
                setVisible(false);
                new Combate().setVisible(true);
            }
        }else{
            if (carrinho > Inicial.jogador.getDinheiro()) { //  checa se o jogador pode comprar
                JOptionPane.showMessageDialog(null, "Vocâ nâo tem dinheiro suficiente", "", JOptionPane.ERROR_MESSAGE);
            } else {
                Inicial.jogador.pagarCompra(carrinho);
                ArrayList<Personagem> perso = Inicial.jogador.getPersonagens();

                itensComprados.forEach(item -> { // distrivui os itens para os personagens
                    if (item.getTipo().equals(perso.get(0).getTipoItem())) {
                        perso.get(0).recebeItem(item.getAtributo());
                    } else if (item.getTipo().equals(perso.get(1).getTipoItem())) {
                        perso.get(1).recebeItem(item.getAtributo());
                    }
                });

                setVisible(false);
                
                // salva e volta para o combate
                manipulaArquivos.salvaJogo(Inicial.jogador);
                new Combate().setVisible(true);
            }
        }
    }//GEN-LAST:event_lblBotaoComprarMouseReleased

