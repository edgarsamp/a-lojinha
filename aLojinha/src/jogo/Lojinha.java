    private void lblItem1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblItem1MouseClicked
        
        if(estoque[0].isNoCarrinho()){ // se o item ja ta no carrinho, tira
            lblItem1Img.setEnabled(false);
            lblItem1.setEnabled(false);

            carrinho -= estoque[0].getValor();
            estoque[0].setNoCarrinho(false);
            
            itensComprados.remove(estoque[0]);
        }else{ // se nao adiciona
            lblItem1Img.setEnabled(true);
            lblItem1.setEnabled(true);
            carrinho += estoque[0].getValor();
            estoque[0].setNoCarrinho(true);
            
            itensComprados.add(estoque[0]);
        }
        
        atualizaDadosLoja();
    }//GEN-LAST:event_lblItem1MouseClicked

