    private void lblBotaoProximoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblBotaoProximoMouseClicked
        
        nome = txtNome.getText();
        
        if(nome.equals("")) // checa se tem nome
            JOptionPane.showMessageDialog(null, "Insira um nome para seu personagem!");
        else if(classe.equals("")) // checa se tem alguma classe selecionada
            JOptionPane.showMessageDialog(null, "Escolha uma classe");
        else{
            // cria o personagem
            perso.setNome(nome);
            perso.setClasse(classe);
            perso.setArmadura(armaduraTemp);
            perso.setForca(forcaTemp);
            perso.setPoder(poderTemp);
            perso.setVida(vidaTemp);
            perso.setVidaMax(vidaTemp+5);
            
            CriarJogador.perso.set(CriarJogador.personagemDaVez, perso);
            setVisible(false);
            new CriarJogador().setVisible(true);
        }
        
        
    }//GEN-LAST:event_lblBotaoProximoMouseClicked

