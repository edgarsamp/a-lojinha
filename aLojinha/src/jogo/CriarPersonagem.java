    private void lblArqueiroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblArqueiroMouseClicked
        // caso tenha escolhido a classe arqueiro, atualiza os os valores para se ajustar a classe
        lblGuerreiro.setEnabled(false);
        lblMago.setEnabled(false);
        lblArqueiro.setEnabled(true);
        classe = "arqueiro";
        if (vidaBonus != 0) 
            vidaTemp -= vidaBonus;
        vidaBonus = 4;

        if (armaduraBonus != 0) 
            armaduraTemp -= armaduraBonus;
        armaduraBonus = 6;

        if (poderBonus != 0) 
            poderTemp -= poderBonus;
        poderBonus = 4;

        if (forcaBonus != 0) 
            forcaTemp -= forcaBonus;
        forcaBonus = 4;
        
        atualizaValores();
    }//GEN-LAST:event_lblArqueiroMouseClicked

