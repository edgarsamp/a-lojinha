    private void geraValoresVida() { // gera dois novos valores para o atributo vida
        
        if(qntDados > 0){
            
            vidaDados = "";

            int dado = gerador.nextInt(5) + 1;
            vidaTemp = dado;
            vidaDados += dado + " + ";

            dado = gerador.nextInt(5) + 1;
            vidaTemp += dado;
            vidaDados += dado + " + ";

            vidaTemp += vidaBonus;
            vidaDados += vidaBonus + " = " + vidaTemp;

            lblVida.setText(vidaDados);
            
            qntDados--;
            lblDados.setText(qntDados + "");

        } else {
            JOptionPane.showMessageDialog(null,"Você nao tem mais dados para rodar os atributos","Vai ter que ser isso mesmo",JOptionPane.ERROR_MESSAGE);
        }

    }
    
